package Skku_Cafe;

//PersonInfo is superclass for UserInfo
//PersonInfo contains only name and mobile information
public class PersonInfo {
	
	private String name;
	private String mobile;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

}
